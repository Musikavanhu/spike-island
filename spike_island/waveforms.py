"""
Spike Island — Waveform Generation and Visualization
=====================================================
Convert discrete spike times into continuous extracellular voltage traces.

Real extracellular electrodes record continuous voltage signals where each
action potential appears as a brief (~1-3 ms) waveform with a characteristic
shape (rapid downstroke, slower upstroke, and undershoot). This module:

- Generates realistic action potential templates
- Superimposes templates at spike times onto a time axis
- Adds configurable noise (Gaussian, 1/f pink noise)
- Renders waveform dashboards with per-neuron traces

References
----------
- Einevoll, G.T. et al. (2013). "How extracellular field potentials
  are generated by cortical neurons." Frontiers in Computational Neuroscience.
- Hadich, F.S. et al. (2020). "Synthetic neural data for machine learning."
"""

from __future__ import annotations

import numpy as np
from scipy.ndimage import gaussian_filter1d
from typing import Any


# ---------------------------------------------------------------------------
# Action potential template generation
# ---------------------------------------------------------------------------

def generate_ap_template(
    dt_ms: float = 0.1,
    duration_ms: float = 2.0,
    peak_amplitude_mv: float = -3.0,
    undershoot_amplitude_mv: float = 1.0,
    peak_time_ms: float = 0.5,
    undershoot_time_ms: float = 1.2,
    upslope_sigma: float = 0.08,
    downslope_sigma: float = 0.15,
) -> tuple[np.ndarray, np.ndarray]:
    """Generate a biphasic action potential template.

    The template is constructed from two Gaussians: a fast downstroke
    (negative deflection) and a slower upstroke (positive undershoot).
    This approximates the signature of a typical extracellular spike
    recorded from an LFP electrode.

    Parameters
    ----------
    dt_ms : float
        Sampling interval in milliseconds (default 0.1 ms → 10 kHz sampling).
    duration_ms : float
        Total template duration in milliseconds.
    peak_amplitude_mv : float
        Peak negative amplitude in millivolts (default -3.0 mV).
    undershoot_amplitude_mv : float
        Positive undershoot amplitude in millivolts (default 1.0 mV).
    peak_time_ms : float
        Time of the downstroke peak relative to spike onset (ms).
    undershoot_time_ms : float
        Time of the upstroke peak relative to spike onset (ms).
    upslope_sigma : float
        Gaussian sigma (in fractional time 0-1) for the downstroke.
    downslope_sigma : float
        Gaussian sigma for the upstroke.

    Returns
    -------
    times : np.ndarray
        Time axis in milliseconds (length N).
    voltage : np.ndarray
        Template voltage in millivolts (length N).

    Notes
    -----
    The template is zero-padded before and after the action potential
    to make convolution-based superposition clean.
    """
    t = np.arange(0.0, duration_ms, dt_ms)

    # Normalized time fractions (0 = onset, 1 = end)
    f = t / duration_ms

    # Downstroke (fast Gaussian centered at peak_time)
    peak_f = peak_time_ms / duration_ms
    downstroke = peak_amplitude_mv * np.exp(
        -0.5 * ((f - peak_f) / upslope_sigma) ** 2
    )

    # Upstroke (slower Gaussian centered at undershoot_time)
    under_f = undershoot_time_ms / duration_ms
    upstroke = undershoot_amplitude_mv * np.exp(
        -0.5 * ((f - under_f) / downslope_sigma) ** 2
    )

    voltage = downstroke + upstroke
    # Ensure clean edges for convolution
    voltage[0] = 0.0
    voltage[-1] = 0.0
    return t, voltage


def generate_triphasic_template(
    dt_ms: float = 0.1,
    duration_ms: float = 3.0,
    peak_amplitude_mv: float = -4.0,
    second_peak_amplitude_mv: float = 0.5,
    t1_ms: float = 0.4,
    t2_ms: float = 1.0,
    t3_ms: float = 2.0,
    sigma: float = 0.1,
) -> tuple[np.ndarray, np.ndarray]:
    """Generate a triphasic action potential template.

    Some electrode configurations (e.g., tetrodes) produce triphasic
    waveforms with a second negative peak after the upstroke.

    Parameters
    ----------
    dt_ms : float
        Sampling interval in milliseconds.
    duration_ms : float
        Total template duration in milliseconds.
    peak_amplitude_mv : float
        Amplitude of the first (primary) negative peak.
    second_peak_amplitude_mv : float
        Amplitude of the second negative peak.
    t1_ms, t2_ms, t3_ms : float
        Timing of the three phases (first downstroke, upstroke, second downstroke).
    sigma : float
        Gaussian sigma (fractional time) for all three peaks.

    Returns
    -------
    times : np.ndarray
        Time axis in milliseconds.
    voltage : np.ndarray
        Template voltage in millivolts.
    """
    t = np.arange(0.0, duration_ms, dt_ms)
    f = t / duration_ms

    def gaussian_peak(amp: float, center_ms: float, sigma_frac: float) -> np.ndarray:
        center_f = center_ms / duration_ms
        return amp * np.exp(-0.5 * ((f - center_f) / sigma_frac) ** 2)

    voltage = (
        gaussian_peak(peak_amplitude_mv, t1_ms, sigma)
        + gaussian_peak(-abs(second_peak_amplitude_mv), t2_ms, sigma * 1.2)
        + gaussian_peak(second_peak_amplitude_mv * 0.3, t3_ms, sigma)
    )

    # Ensure clean edges
    voltage[0] = 0.0
    voltage[-1] = 0.0

    return t, voltage


# ---------------------------------------------------------------------------
# Spike-to-waveform conversion
# ---------------------------------------------------------------------------

def spikes_to_waveform(
    spike_times: np.ndarray,
    template: np.ndarray,
    template_dt: float = 0.1,
    duration_ms: float = 5_000.0,
    sampling_hz: float = 10_000.0,
    noise_std_mv: float = 0.0,
    noise_type: str = "gaussian",
    seed: int | None = None,
) -> tuple[np.ndarray, np.ndarray]:
    """Convert discrete spike times into a continuous voltage trace.

    Each spike is represented by superimposing the template waveform
    at the corresponding spike time on a zero-baseline signal.

    Parameters
    ----------
    spike_times : np.ndarray
        Sorted spike times in milliseconds.
    template : np.ndarray
        Voltage template in millivolts (from :func:`generate_ap_template`
        or :func:`generate_triphasic_template`).
    template_dt : float
        Sampling interval of the template in ms.
    duration_ms : float
        Total trace duration in milliseconds.
    sampling_hz : float
        Output sampling rate in Hz (default 10 kHz → 0.1 ms bins).
    noise_std_mv : float
        Standard deviation of additive noise in mV (default 0).
    noise_type : str
        Noise model: "gaussian" (white) or "pink" (1/f).
    seed : int or None
        Random seed for noise generation.

    Returns
    -------
    times : np.ndarray
        Time axis in milliseconds.
    voltage : np.ndarray
        Continuous voltage trace in millivolts.

    Notes
    -----
    Uses :func:`scipy.ndimage.convolve` under the hood for efficient
    template superposition. The spike train is binned at the template
    resolution then convolved with the template.
    """
    from scipy.ndimage import convolve1d

    rng = np.random.default_rng(seed)

    # Output time axis (convert Hz → ms)
    output_dt_ms = 1000.0 / sampling_hz
    times = np.arange(0.0, duration_ms, output_dt_ms)

    # Create a binned spike train at template resolution
    template_bins = len(template)
    bin_width = template_dt
    n_bins = int(np.ceil(duration_ms / bin_width))
    spike_train = np.zeros(n_bins, dtype=np.float64)

    # Map spike times to bins
    if len(spike_times) > 0:
        bin_indices = np.floor(spike_times / bin_width).astype(int)
        bin_indices = np.clip(bin_indices, 0, n_bins - 1)
        spike_train[bin_indices] = 1.0

    # Convolve with template (template is normalized to its peak)
    # This superimposes the template at each spike time
    if n_bins < template_bins:
        spike_train = np.pad(spike_train, (0, template_bins - n_bins))
        convolved = convolve1d(spike_train, template, mode="constant")
        convolved = convolved[:n_bins]
    else:
        convolved = convolve1d(spike_train, template, mode="constant")

    # Resample to output sampling rate if needed
    if output_dt_ms != bin_width:
        # Interpolate using linear method
        convolved_times = np.arange(0.0, len(convolved) * bin_width, bin_width)
        voltage = np.interp(times, convolved_times[:len(convolved)], convolved)
    else:
        voltage = convolved[: len(times)]

    # Add noise
    if noise_std_mv > 0:
        if noise_type == "gaussian":
            noise = rng.normal(0, noise_std_mv, size=len(times))
        elif noise_type == "pink":
            noise = _generate_pink_noise(len(times), rng) * noise_std_mv
        else:
            raise ValueError(f"Unknown noise_type: {noise_type}")
        voltage += noise

    return times, voltage


def _generate_pink_noise(length: int, rng: np.random.Generator) -> np.ndarray:
    """Generate 1/f (pink) noise using FFT filtering.

    Pink noise has a power spectral density proportional to 1/f,
    which approximates the background noise characteristics of
    neural recordings (thermal noise + physiological background).

    Parameters
    ----------
    length : int
        Number of samples.
    rng : np.random.Generator
        Random number generator.

    Returns
    -------
    np.ndarray
        Pink noise samples (zero-mean).
    """
    white = rng.normal(0, 1, size=2 * length)
    fft = np.fft.fft(white)
    # 1/f power spectrum → 1/sqrt(f) amplitude spectrum
    n_freqs = len(fft)
    freqs = np.fft.fftfreq(n_freqs)
    # Avoid division by zero at DC
    pink_spectrum = fft / (np.abs(freqs) + 1.0) ** 0.5
    # Apply a low-pass to remove DC and very high freq artifacts
    low_pass = np.ones(n_freqs)
    dc_idx = 0
    nyquist_idx = n_freqs // 2
    low_pass[dc_idx] = 0.0  # remove DC
    pink_filtered = pink_spectrum * low_pass
    result = np.fft.ifft(pink_filtered).real[:length]
    return result


