# Utility: compute waveform statistics
# ---------------------------------------------------------------------------

def waveform_statistics(
    voltage: np.ndarray,
    times: np.ndarray | None = None,
) -> dict[str, Any]:
    """Compute summary statistics from a voltage trace.

    Parameters
    ----------
    voltage : np.ndarray
        Voltage trace in millivolts.
    times : np.ndarray or None
        Corresponding time axis in milliseconds.

    Returns
    -------
    dict
        Statistics dictionary with keys:
        - ``rms_mv``: RMS voltage
        - ``peak_to_peak_mv``: Peak-to-peak amplitude
        - ``max_mv``: Maximum voltage
        - ``min_mv``: Minimum voltage
        - ``snr_db``: Signal-to-noise ratio (signal = template peaks, noise = baseline)
        - ``mean_voltage_mv``: Mean voltage (should be near 0 for balanced traces)
        - ``duration_ms``: Trace duration in ms
        - ``samples``: Number of samples
    """
    rms = float(np.sqrt(np.mean(voltage ** 2)))
    peak_to_peak = float(np.max(voltage) - np.min(voltage))
    mean_v = float(np.mean(voltage))
    duration = float(times[-1] - times[0]) if times is not None and len(times) > 1 else 0.0

    # SNR: estimate signal power from peaks, noise from baseline
    abs_voltage = np.abs(voltage)
    # Baseline: values below 20th percentile (likely noise-only regions)
    threshold = np.percentile(abs_voltage, 20)
    baseline_mask = abs_voltage < threshold
    if np.sum(baseline_mask) > 10:
        noise_power = float(np.mean(voltage[baseline_mask] ** 2))
    else:
        noise_power = float(np.std(voltage) ** 2)

    signal_power = max(rms ** 2 - noise_power, 0.0)
    if noise_power > 0:
        snr_db = 10.0 * np.log10(signal_power / noise_power + 1e-10)
    else:
        snr_db = float("inf")

    return {
        "rms_mv": rms,
        "peak_to_peak_mv": peak_to_peak,
        "max_mv": float(np.max(voltage)),
        "min_mv": float(np.min(voltage)),
        "mean_voltage_mv": mean_v,
        "snr_db": float(snr_db),
        "duration_ms": duration,
        "samples": len(voltage),
    }


def detect_spikes_from_waveform(
    voltage: np.ndarray,
    threshold_mv: float | None = None,
) -> np.ndarray:
    """Detect spike events from a continuous voltage trace.

    Uses a threshold-based detector: finds local minima below a threshold.
    This is the simplest (and most common) spike detection method used in
    extracellular recordings.

    Parameters
    ----------
    voltage : np.ndarray
        Voltage trace in millivolts.
    threshold_mv : float or None
        Voltage threshold for detection. Defaults to 3× the
        standard deviation of the absolute voltage (negative direction).

    Returns
    -------
    np.ndarray
        Indices of detected spike peaks (local minima below threshold).

    Notes
    -----
    This is a simplified detector. Real spike sorting pipelines use
    more sophisticated methods (template matching, PCA projection,
    or clustering-based approaches like Kilosort). The threshold-based
    approach is included as a pedagogical baseline.

    The returned indices can be converted to times using the original
    time axis: ``times[detect_spikes_from_waveform(voltage)]``.
    """
    if threshold_mv is None:
        threshold_mv = -3.0 * np.std(voltage)

    # Find local minima
    minima = np.zeros_like(voltage, dtype=bool)
    minima[1:-1] = (voltage[1:-1] < voltage[:-2]) & (voltage[1:-1] < voltage[2:])
    minima[0] = voltage[0] < voltage[1]
    minima[-1] = voltage[-1] < voltage[-2]

    # Keep minima below threshold
    below_threshold = voltage < threshold_mv
    spike_indices = np.where(minima & below_threshold)[0]

    # Refractory filtering: keep only the deepest spike within 2 ms windows
    if len(spike_indices) > 1:
        keep = [spike_indices[0]]
        for idx in spike_indices[1:]:
            if idx - keep[-1] > 20:  # ~2 ms at 10 kHz sampling
                keep.append(idx)
        spike_indices = np.array(keep)

    return spike_indices
