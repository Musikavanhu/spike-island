def plot_waveform_dashboard(
    results: list[dict],
    duration_ms: float = 5_000.0,
    template: np.ndarray | None = None,
    template_dt: float = 0.1,
    sampling_hz: float = 10_000.0,
    noise_std_mv: float = 0.05,
    noise_type: str = "gaussian",
    seed: int = 42,
    save_path: str = "plots/waveform_dashboard.png",
    window_ms: float = 500.0,
) -> None:
    """Plot waveform traces for each neuron with spike raster and template.

    Generates a dashboard showing:
    - Row 1: Template waveform (the AP shape used)
    - Per neuron: Continuous voltage trace with a zoomed-in window
    - Per neuron: Spike raster aligned with the trace

    Parameters
    ----------
    results : list[dict]
        Analysis results from :func:`spike_island.analysis.analyze`,
        each containing at minimum 'name' and the original spike_times.
        Each dict should have a 'spike_times' key (np.ndarray of spike times in ms).
    duration_ms : float
        Total simulation duration in ms.
    template : np.ndarray or None
        AP template voltages. If None, generates a default biphasic template.
    template_dt : float
        Template sampling interval in ms.
    sampling_hz : float
        Output waveform sampling rate in Hz.
    noise_std_mv : float
        Noise standard deviation in mV.
    noise_type : str
        "gaussian" or "pink" noise.
    seed : int
        Random seed for noise generation.
    save_path : str
        Output PNG path.
    window_ms : float
        Duration of the zoomed-in waveform window shown per neuron.

    Notes
    -----
    This is the core visualization for showing what extracellular recordings
    look like for different firing patterns. The voltage traces reveal
    burst structure, regularity, and noise characteristics that ISI analysis
    alone cannot capture.
    """
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    # Generate default template if not provided
    if template is None:
        _, template = generate_ap_template(
            dt_ms=0.1,
            duration_ms=2.0,
            peak_amplitude_mv=-3.0,
            undershoot_amplitude_mv=1.0,
        )

    # Color palette for neurons
    colors = ["#1e3a5f", "#c45533", "#2d7d46", "#9b59b6", "#e67e22", "#1abc9c"]
    n = len(results)

    fig_height = 1.5 + 2.0 * n  # template row + 2 rows per neuron
    fig = plt.figure(figsize=(12, fig_height))

    # Row 0: Template
    total_rows = 1 + 2 * n
    template_ax = fig.add_subplot(total_rows, 1, 1)
    _, template_t = generate_ap_template()
    template_ax.plot(template_t, template, color="#222", linewidth=1.5)
    template_ax.set_ylabel("Voltage (mV)", fontsize=9)
    template_ax.set_xlabel("Time (ms)", fontsize=9)
    template_ax.set_title("AP Template", fontsize=9, fontweight="bold")
    template_ax.fill_between(template_t, template, alpha=0.2)
    template_ax.axhline(0, color="#ccc", linewidth=0.5)

    for i, res in enumerate(results):
        color = colors[i % len(colors)]
        name = res.get("name", f"Neuron {i + 1}")

        # Get spike times from result
        spike_times = res.get("spike_times", np.array([]))

        # Generate waveform
        times, voltage = spikes_to_waveform(
            spike_times,
            template,
            template_dt=template_dt,
            duration_ms=duration_ms,
            sampling_hz=sampling_hz,
            noise_std_mv=noise_std_mv,
            noise_type=noise_type,
            seed=seed + i + 1,
        )

        # Find a window with interesting activity
        if len(spike_times) >= 2:
            # Pick a window around the middle spike(s)
            mid_spike = spike_times[len(spike_times) // 2]
            t_start = max(0.0, mid_spike - window_ms / 2)
            t_end = t_start + window_ms
        else:
            t_start = 0.0
            t_end = min(window_ms, duration_ms)

        # Waveform trace (zoomed window) — row 2+2*i (1-indexed)
        trace_row = 2 + 2 * i
        trace_ax = fig.add_subplot(total_rows, 1, trace_row)
        mask = (times >= t_start) & (times < t_end)
        trace_ax.plot(times[mask] - t_start, voltage[mask], color=color, linewidth=0.6)
        trace_ax.set_ylabel("Voltage (mV)", fontsize=8)
        trace_ax.set_xlabel(f"Time (ms) — {name}", fontsize=8)
        trace_ax.set_ylim(-5, 5)
        trace_ax.axhline(0, color="#eee", linewidth=0.5)

        # Mark spike times in the window
        window_spikes = spike_times[(spike_times >= t_start) & (spike_times < t_end)]
        for st in window_spikes:
            trace_ax.axvline(st - t_start, color=color, alpha=0.3, linewidth=0.5, linestyle="--")

        # Raster — row 3+2*i
        raster_row = 3 + 2 * i
        raster_ax = fig.add_subplot(total_rows, 1, raster_row, sharex=trace_ax)
        for st in spike_times:
            adjusted = st - t_start
            if 0 <= adjusted < window_ms:
                raster_ax.plot([adjusted, adjusted], [0, 1], color=color, linewidth=1.2)
        raster_ax.set_xlim(0, window_ms)
        raster_ax.set_ylim(-0.1, 1.2)
        raster_ax.set_xticks([])
        raster_ax.set_yticks([])

    fig.suptitle(
        f"Waveform Dashboard — {n} neuron(s) · {sampling_hz/1000:.0f} kHz sampling",
        fontsize=12,
        fontweight="bold",
        y=0.98,
    )
    plt.tight_layout(rect=[0, 0, 1, 0.96])
    plt.savefig(save_path, dpi=150)
    print(f"Waveform dashboard saved to {save_path}")


